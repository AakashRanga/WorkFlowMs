<script>
    document.addEventListener('DOMContentLoaded', function () {
        const showPapersBtn = document.getElementById('showPapersBtn');
        const clearFiltersBtn = document.getElementById('clearFiltersBtn');
        const papersBoxes1 = document.getElementById('papersBoxes1');
        const papersBoxes2 = document.getElementById('papersBoxes2');
        const warningMessage = document.getElementById('warningMessage');

        function areAnyCheckboxesChecked() {
            const checkboxes = document.querySelectorAll('input[type="checkbox"]');
            return Array.from(checkboxes).some(checkbox => checkbox.checked);
        }

        function clearAllFilters() {
            // Uncheck all checkboxes
            document.querySelectorAll('input[type="checkbox"]').forEach(checkbox => checkbox.checked = false);

            // Hide the warning message
            warningMessage.style.display = 'none';

            // Hide the papers boxes
            papersBoxes1.style.display = 'none';
            papersBoxes2.style.display = 'none';

            // Reset button text
            showPapersBtn.textContent = 'SHOW CALL FOR PAPER';
        }

        showPapersBtn.addEventListener('click', function () {
            if (areAnyCheckboxesChecked()) {
                // Toggle visibility of boxes
                if (papersBoxes1.style.display === 'none' && papersBoxes2.style.display === 'none') {
                    papersBoxes1.style.display = 'flex';
                    papersBoxes2.style.display = 'flex';
                    showPapersBtn.textContent = 'HIDE CALL FOR PAPER';
                    warningMessage.style.display = 'none';
                } else {
                    papersBoxes1.style.display = 'none';
                    papersBoxes2.style.display = 'none';
                    showPapersBtn.textContent = 'SHOW CALL FOR PAPER';
                }
            } else {
                warningMessage.style.display = 'block';
                papersBoxes1.style.display = 'none';
                papersBoxes2.style.display = 'none';
                showPapersBtn.textContent = 'SHOW CALL FOR PAPER';
            }
        });

        clearFiltersBtn.addEventListener('click', function () {
            clearAllFilters();
        });
    });

</script>